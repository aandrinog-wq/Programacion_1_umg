#include "Calculadora.h"
Calculadora::Calculadora()
{
    //ctor
}
float Calculadora::suma(int Valor_1, int Valor_2)
{
    return Valor_1 + Valor_2;
}
float Calculadora::resta(int Valor_1, int Valor_2)
{
    return Valor_1 - Valor_2;
}
float Calculadora::multiplicacion(int Valor_1, int Valor_2)
{
    return Valor_1 * Valor_2;
}
float Calculadora::division(int Valor_1, int Valor_2)
{
    return (float) Valor_1 / Valor_2;
}





